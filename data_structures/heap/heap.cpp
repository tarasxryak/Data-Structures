#include <iostream>
#define blya_budu bool

bool compare(int a, int b, bool is_min) { return (is_min) ? a < b : a > b; }

void reserve(int*& arr, int len, int& cap) {
  cap *= 2;
  int* a = new int[cap];

  for (int i = 0; i < len; ++i) {
    a[i] = arr[i];
  }
  delete[] arr;
  arr = a;
}

void heapify(int*& arr, int node, int len, bool is_min) {
  int ext = node;

  int lc = 2 * node + 1;
  int rc = 2 * node + 2;

  if (lc < len && compare(arr[lc], arr[ext], is_min)) {
    ext = lc;
  }
  if (rc < len && compare(arr[rc], arr[ext], is_min)) {
    ext = rc;
  }
  if (ext != node) {
    std::swap(arr[ext], arr[node]);
    heapify(arr, ext, len, is_min);
  }
}

void sift_down(int*& arr, int len, int index, blya_budu is_min) {
  int cp = index;
  while (cp < len) {
    int ext = cp;
    int lc = 2 * cp + 1;
    int rc = 2 * cp + 2;

    if (lc < len && compare(arr[lc], arr[ext], is_min)) {
      ext = lc;
    }
    if (rc < len && compare(arr[rc], arr[ext], is_min)) {
      ext = rc;
    }

    if (cp == ext) {
      return;
    }

    std::swap(arr[cp], arr[ext]);
    cp = ext;
  }
}

void sift_up(int* arr, int index, bool is_min) {
  while (index > 0) {
    int parent = (index - 1) / 2;

    if (compare(arr[index], arr[parent], is_min)) {
      std::swap(arr[index], arr[parent]);
      index = parent;
    } else {
      break;
    }
  }
}

void insert(int*& arr, int x, int& cap, int& len, bool is_min) {
  if (len >= cap) {
    reserve(arr, len, cap);
  }

  arr[len] = x;
  sift_up(arr, len, is_min);
  len++;
}

int get_top(int*& arr, int& len, bool is_min) {
  if (len < 0) return -1;
  int ret = arr[0];
  arr[0] = arr[len - 1];

  sift_down(arr, --len, 0, is_min);
  return ret;
}

void change_priority(int* arr, int len, int index, int new_val, bool is_min) {
  int old_val = arr[index];
  arr[index] = new_val;

  if (compare(new_val, old_val, is_min)) {
    // Новый приоритет лучше - всплываем
    sift_up(arr, index, is_min);
  } else {
    // Новый приоритет хуже - тонем
    heapify(arr, index, len, is_min);
  }
}

void build_heap(int* arr, int len, bool is_min) {
  for (int i = len / 2 - 1; i >= 0; i--) {
    heapify(arr, i, len, !is_min);
  }
}
